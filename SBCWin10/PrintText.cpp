CString CPrintText::GetString(unsigned char *&data, int &length)
{
	CString result = "";
	int count= *data++;
	int strlen;
	/*	if(CChildView::Language_Index>Asian_Language_Start)
	strlen=count/2;
	else*/
	strlen=count;
	for (int i = 0; i < strlen; i++)
	{
		union {
			unsigned char ch[2];
			TCHAR character;
		}u;

		/*if(CChildView::Language_Index>Asian_Language_Start)
		u.ch[1]= *data++;
		else	*/	
		u.ch[1]=NULL;
		u.ch[0]= *data++;
		result += (u.character);
	}
	length -= count+1;
	return result;
}

/**************************************************************************/
/* This function convert two character bytes into one integer byte.       */
/**************************************************************************/
int CPrintText::Convert_Char_To_Integer(unsigned char *&data, int &length)
{
	union {
		int i;
		char c[INTEGERLENGTH];
	} u;
	for (int k = 0; k < CORDLENGTH; k++)
	{
		u.c[k] = 0;
	}
	for (int j = CORDLENGTH; j < INTEGERLENGTH; j++)
	{
		u.c[j] =*data++;
		length--;
	}
	return (ntohl(u.i ));
}

/*****************************************************************/
/* this function is used for view preset screen.it removes null  */
/* from the strings and put a" = "in place of all null.			 */
/*****************************************************************/
CString CPrintText::RemoveNull(CString str)
{
	int found =0;
	bool Found =false;
	int Temp=0;
	while(found!=(-1))
	{	
		CString str2="     ";
		found = str.Find(str2);
		if(found != -1)
		{
			str.Delete(found,5);
			Found=true;
			Temp=found; 
		}
	}
	if((Found)&&(Temp!=0))
		str.Insert( Temp,_T("  =  ")  );
	return str;
}