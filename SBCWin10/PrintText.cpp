CString CPrintText::VGAFormatString(CString str1,CString str2)
{
	int NumDisplay = PRINTSPACEPAGE; /* 35 space of printable area on page for each half  */
	int i, y, String1LenA;
	CString VGABuffer;

	VGABuffer=str1;
	/*  total number of char between */  
	String1LenA = str1.GetLength();
	y = String1LenA + str2.GetLength();

	y = NumDisplay - y;     /*  Number of blanks between strings      */

	/* Adjust usable part of String1 -- for strings that are too long */
	if(y < 1)
	{
		String1LenA += (y-1);
		y = String1LenA + 1;
	}
	else
		y = y + String1LenA;

	for(i=String1LenA; i < y; i++)   /* calculate blank spaces between strings */
		VGABuffer+=" ";      /* Add blank space to the string          */

	VGABuffer+=str2;
	return VGABuffer;
}


